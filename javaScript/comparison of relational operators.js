

// relational operators


let number = 5;
let number1 = '5';
console.log(number);
console.log(number1);


// equality operators ==
// only compare values, and if does conversion if necessary

console.log("number == number1 -> ",number == number1);
console.log(`${number} == ${number1} → ${number==number1}`)


//  strict equality operator strict or exact
// compare value and data type ===
console.log(`${number} === ${number1} → ${number===number1}`)


// distinct operator
console.log(`${number} != ${number1} → ${number!=number1}`);

// operator distinct or exact  
console.log(`${number}!==${number1} → ${number!==number1}`);

// operator less than....
console.log(`${number} < ${number1} → ${number<number1}`);

// less than or equal to operator 
console.log(`${number} <= ${number1} → ${number <= number1}}`);

// operator greater than

console.log(`${number} > ${number1} → ${number > number1}}`);

// greater than or equal to operator
console.log(`${number} >= ${number1} → ${number >= number1}}`);
